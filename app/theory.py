#  difference between Pydantic and ORM

# Pydantic is a library for data validation and parsing, commonly used to define and validate data models.
# ORM (Object-Relational Mapping) libraries are used to interact with databases by mapping database tables to Python classes, abstracting away direct SQL queries.

#  Schema/Pydantic model is used to define the structure of a request and response.
#  SQLAlchemy Model is used to define the structure of a database table and  used to query the database.